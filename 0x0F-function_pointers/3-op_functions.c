#include <stdio.h>
/**
 * op_add - add two integers
 * @a: first integer
 * @b: second integer
 * Return: integer value
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract two integers
 * @a: integer value
 * @b: integer value
 * Return: integer value
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two integers
 * @a: integer value
 * @b: integer value
 * Return: integer value
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two integer values
 * @a: integer value
 * @b: integer value
 * Return: integer value
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - return the module b of a
 * @a: integer value
 * @b: integer value
 * Return: integer value
 */
int op_mod(int a, int b)
{
	return (a % b);
}
